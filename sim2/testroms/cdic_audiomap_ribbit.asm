	move.w #2304,$8000400a  ; Size is one sector
	move.b #$12,$80004005 ; Mem. to Dev., 16 Bit Words, 
	move.b #$80,$80004007 ; start DMA
	move.w #$F20C,$303FF8 ; DMA Control Register = Start Transmission at 320C

	move.w $302800,d7
	move.w $302802,d7
	move.w $302808,d7
	move.w $30280a,d7
	move.w $303208,d7
	move.w $30320a,d7
	move.w $303200,d7
	move.w $303202,d7

	rts

wait:
	add #-1,d0
	bne wait
	rts

